package bricker.brick_strategies;


/**
 *  Enumerates the types of behaviors available in the game.
 */
public enum BehaviorsTypes {
    /**
     *  Represents a basic behavior.
     */
 BASIC,
    /**
     *  Represents a puck behavior.
     */
 PUCK,
    /**
     *  Represents a paddle behavior.
     */
 PADDLE,
    /**
     *  Represents a heart behavior.
     */
 HEART,
    /**
     *  Represents a camera behavior.
     */
 CAMERA,
    /**
     *  Represents a double behavior.
     */
 DOUBLE }

